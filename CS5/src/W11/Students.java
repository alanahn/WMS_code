package W11;

class Students{
    private String name;
    private int num;
    private String grade;
    private String remark;
    
    public Students(String name, int num, String grade, String remark) {
        this.name = name;
        this.num = num;
        this.grade = grade;
        this.remark = remark;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getNum() {
        return num;
    }
    public void setNum(int num) {
        this.num = num;
    }
    public String getGrade() {
        return grade;
    }
    public void setGrade(String grade) {
        this.grade = grade;
    }
    public String getRemark() {
        return remark;
    }
    public void setRemark(String remark) {
        this.remark = remark;
    }
}

